const SUDOKU_SOURCES = [
  {
    name: "websudoku.com",
    sudokus: WEBSUDOKU_SUDOKUS,
  },
  {
    name: "sudoku.com",
    sudokus: SUDOKUDOTCOM_SUDOKUS,
  },
]

interface SudokusBenchmark {
  sudoku: string
  index: number
  iterations: number
}

function benchmarSudokus(
  sudokus: SudokuGrid[],
  strategy: (sudoku: SudokuGrid) => {
    newSudokus: SudokuGrid[]
    iterations: number
  },
): SudokusBenchmark[] {
  const solvedSudokus = sudokus.map((sudoku, i) => {
    return { ...dfsLoop([sudoku], strategy, 0), index: i, sudoku: sudoku }
  })

  if (solvedSudokus.some((s) => s.solvedSudoku === null)) {
    throw Error("A sudoku could not be solved")
  }

  return solvedSudokus.map((s) => {
    return {
      sudoku: stringifySudoku(s.sudoku),
      index: s.index,
      iterations: s.iterations,
    }
  })
}

export interface SudokusWithLevelsBenchmark {
  benchmarks: SudokusBenchmark[]
  level: number
}

function benchmarkSudokusWithLevels(
  sudokusWithLevels: SudokusWithLevel[],
  strategy: (sudoku: SudokuGrid) => {
    newSudokus: SudokuGrid[]
    iterations: number
  },
): SudokusWithLevelsBenchmark[] {
  return sudokusWithLevels.map((sudokusWithLevel) => {
    const parsedSudokus = sudokusWithLevel.sudokus.map((s) => parseSudoku(s))
    return {
      benchmarks: benchmarSudokus(parsedSudokus, strategy),
      level: sudokusWithLevel.level,
    }
  })
}

interface SourceLevelBenchmark {
  sourceName: string
  levelBenchmarks: SudokusWithLevelsBenchmark[]
}

export interface BenchmarkResult {
  name: string
  sources: SourceLevelBenchmark[]
}

export async function runBenchmarks(): Promise<BenchmarkResult[]> {
  const strategies = [
    {
      name: "AC3",
      strategy: AC3Strategy,
    },
    {
      name: "MinimumRemainingValue",
      strategy: minimumRemainingValueStrategy,
    },
  ]

  return strategies.map(({ strategy, name }) => {
    return {
      name,
      sources: SUDOKU_SOURCES.map((ss) => {
        return {
          sourceName: ss.name,
          levelBenchmarks: benchmarkSudokusWithLevels(ss.sudokus, strategy),
        }
      }),
    }
  })
}

export function resultsToCsv(results: BenchmarkResult[]): string {
  const header = ["strategy", "source", "level", "sudokuIndex", "iterations"]
  const csvLines = [header.join(",")]

  for (const result of results) {
    for (const r of result.sources) {
      for (const lb of r.levelBenchmarks) {
        for (const b of lb.benchmarks) {
          csvLines.push(
            [result.name, r.sourceName, lb.level, b.index, b.iterations].join(
              ",",
            ),
          )
        }
      }
    }
  }

  return csvLines.join("\n")
}
